# Mulai
jari_jari = float(input("Masukkan jari-jari lingkaran: "))  # Input jari-jari dari pengguna
phi = 3.14  # Input nilai phi

luas_permukaan = 4 * phi * jari_jari * jari_jari  # Hitung luas permukaan
volume = 4/3 * phi * jari_jari * jari_jari * jari_jari  # Hitung volume

print("Luas Permukaan Bola: ", luas_permukaan)  # Tampilkan luas permukaan
print("Volume Bola: ", volume)  # Tampilkan volume
# Selesai.
